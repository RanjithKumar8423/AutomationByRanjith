package firstprogrampackage;

public class SwitchCasesVowels {

	public static void main(String[] args) {
	char c='m';
	switch (c) {
	case 'a':
		System.out.println("vowels"+"   "+c);
		break;
	case 'e':
		System.out.println("vowels"+c);
		break;
	case 'i':
		System.out.println("vowels"+c);
		break;
	case 'o':
		System.out.println("vowels"+c);
		break;
	case 'u':
		System.out.println("vowels"+c);
		break;

	default:
		System.out.println("Not a vowels"+"   "+c);
		break;
	}

	}

}
